    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
       int selectedRowIndex = tblStudent.getSelectedRow();
    if (selectedRowIndex < 0) {
        JOptionPane.showMessageDialog(this,
                "Please select a student.",
                "Warning",
                JOptionPane.WARNING_MESSAGE);
        return;
    }

    DefaultTableModel model = (DefaultTableModel) tblStudent.getModel();
    UserAccount ua = (UserAccount) model.getValueAt(selectedRowIndex, 0);

    StudentProfile sp = (StudentProfile) ua.getAssociatedPersonProfile();

    AdminStudents panel = new AdminStudents(sp, ua, business, userProcessContainer, this);
    userProcessContainer.add("AdminStudents", panel);

    CardLayout layout = (CardLayout) userProcessContainer.getLayout();
    layout.next(userProcessContainer);  
    }//GEN-LAST:event_btnNextActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
  int selectedRowIndex = tblStudent.getSelectedRow();
    if (selectedRowIndex < 0) {
        JOptionPane.showMessageDialog(this,
                "Please select a student to delete.",
                "Warning",
                JOptionPane.WARNING_MESSAGE);
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this,
            "Are you sure you want to delete this student account?",
            "Confirm Delete",
            JOptionPane.YES_NO_OPTION);

    if (confirm != JOptionPane.YES_OPTION) return;

    DefaultTableModel model = (DefaultTableModel) tblStudent.getModel();
    UserAccount ua = (UserAccount) model.getValueAt(selectedRowIndex, 0);

    business.getUserAccountDirectory().removeUserAccount(ua);

    JOptionPane.showMessageDialog(this, "Student account deleted successfully.");

    populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed


