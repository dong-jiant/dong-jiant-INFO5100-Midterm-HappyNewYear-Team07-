    void populateTable() {
   DefaultTableModel model = (DefaultTableModel) tblStudent.getModel();
    model.setRowCount(0);

    for (UserAccount ua : business.getUserAccountDirectory().getUserAccountList()) {

        Profile prof = ua.getAssociatedPersonProfile();
        if (!(prof instanceof StudentProfile)) continue;

        Person p = prof.getPerson();

        Object[] row = new Object[6];
        row[0] = ua;        
        row[1] = (p == null || p.getName() == null) ? "" : p.getName();
        row[2] = ua.getUserLoginName();     
        row[3] = "Active";
        row[4] = ua.getLastAccessTime();
        row[5] = ua.getLastUpdatedTime();

        model.addRow(row);
    }
    }
