    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
 if (userAccount == null) {
        JOptionPane.showMessageDialog(this,"No faculty account selected.","Error",JOptionPane.ERROR_MESSAGE);
        return;
    }

    Profile profile = userAccount.getAssociatedPersonProfile();
    Person p = (profile == null) ? null : profile.getPerson();

    if (p == null) {
        JOptionPane.showMessageDialog(this,"Faculty person record not found.","Error",JOptionPane.ERROR_MESSAGE);
        return;
    }

    String name = txtName.getText().trim();
    String email = txtEmail.getText().trim();
    String dept = txtDepartment.getText().trim();
    String newPw = txtNewPassword.getText().trim();

    if (name.isEmpty()) {
        JOptionPane.showMessageDialog(this,"Name is required.","Warning",JOptionPane.WARNING_MESSAGE);
        return;
    }

    if (email.isEmpty()) {
        JOptionPane.showMessageDialog(this,"Email is required.","Warning",JOptionPane.WARNING_MESSAGE);
        return;
    }

    p.setName(name);
    p.setEmail(email);
    p.setDepartment(dept);

    if (!newPw.isEmpty()) {
        userAccount.setPassword(newPw); 
        txtNewPassword.setText("");
    } else {
        userAccount.touchLastUpdated();
    }

    populateFields();

    JOptionPane.showMessageDialog(this,"Updated successfully.","Success",JOptionPane.INFORMATION_MESSAGE);

    if (parentPanel != null) {
        parentPanel.populateTable();
    }


    userProcessContainer.remove(this);
    CardLayout layout = (CardLayout) userProcessContainer.getLayout();
    layout.previous(userProcessContainer);
        }//GEN-LAST:event_btnUpdateActionPerformed


