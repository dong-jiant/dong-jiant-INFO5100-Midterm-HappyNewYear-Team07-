    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
   int confirm = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to delete this faculty account?",
            "Confirm Delete",
            JOptionPane.YES_NO_OPTION
    );

    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }

    Profile profile = userAccount.getAssociatedPersonProfile();

    if (profile instanceof FacultyProfile) {
        FacultyProfile fp = (FacultyProfile) profile;
        business.getFacultyDirectory().removeFacultyProfile(fp);
    }

    business.getUserAccountDirectory().getUserAccountList().remove(userAccount);

    JOptionPane.showMessageDialog(this,
            "Faculty account deleted successfully.",
            "Deleted",
            JOptionPane.INFORMATION_MESSAGE);

    if (parentPanel != null) {
        parentPanel.populateTable();
    }

    userProcessContainer.remove(this);
    CardLayout layout = (CardLayout) userProcessContainer.getLayout();
    layout.previous(userProcessContainer);
    }//GEN-LAST:event_btnDeleteActionPerformed

