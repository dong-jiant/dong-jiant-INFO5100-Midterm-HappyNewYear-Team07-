    private void loadCourses() {
        if (faculty == null || faculty.getPerson() == null) {
            return;
        }
        
        CourseDirectory cd = business.getCourseDirectory();
        ArrayList<CourseInfo> courses = cd.getCoursesForFaculty(faculty.getPerson().getPersonId());
        
        jComboBox1.removeAllItems();
        
        for (CourseInfo course : courses) {
            jComboBox1.addItem(course.getCourseId() + " - " + course.getCourseName());
        }
    }

