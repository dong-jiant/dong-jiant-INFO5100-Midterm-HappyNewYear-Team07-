    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        String selected = (String) jComboBox1.getSelectedItem();
        
        if (selected == null || selected.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a course", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String courseId = selected.split(" - ")[0];
        
        EnrollmentDirectory ed = business.getEnrollmentDirectory();
        ArrayList<Enrollment> enrollments = ed.getEnrollmentsForStudent("");
        
        DefaultTableModel model = (DefaultTableModel) tblCourses.getModel();
        model.setRowCount(0);
        
        StudentDirectory sd = business.getStudentDirectory();
        ArrayList<StudentProfile> allStudents = sd.getStudentList();
        
        for (StudentProfile sp : allStudents) {
            if (sp.getPerson() != null) {
                String studentId = sp.getPerson().getPersonId();
                ArrayList<Enrollment> studentEnrollments = ed.getEnrollmentsForStudent(studentId);
                
                for (Enrollment e : studentEnrollments) {
                    if (e.getCourseId().equals(courseId) && e.getGradePoint() != null) {
                        String status = e.getGradePoint() >= 1.0 ? "Pass" : "Fail";
                        
                        model.addRow(new Object[]{
                            studentId,
                            studentId,
                            String.format("%.2f", e.getGradePoint()),
                            status
                        });
                    }
                }
            }
        }
        
        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "No grades found for this course", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }


